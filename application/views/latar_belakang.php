			Latar Belakang Program Studi S2 Matematika : <br>
			Matematika, tidak dapat disangkal lagi, merupakan servant of science and technology. Perkembangan sains dan teknologi didukung oleh matematika, sebaliknya juga berlaku, semakin tinggi sains dan teknologi yang dipelajari maka matematikanya pun juga membutuhkan matematika dengan level yang semakin tinggi pula.  Berbagai fenomena alam yang dipelajari oleh sains memerlukan matematika sebagai kunci dan sebagai bahasa dalam mempelajari fenomena tersebut. Demikian pula teknologii yang membantu manusia dalam menghadapii kendala yang disebabkan oleh karena alam dan karena  manusia sendiri, membutuhkan matematika.
Pengembangan keilmuan di bidang ekonomi, rekayasa, lingkungan, Fisika, Kimia, Biologi, Kedokteran, Teknologi informasi dan komunikasi, serta di bidang ilmu lainnya sangat  memerlukan pemodelan matematika.


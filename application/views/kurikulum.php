			<div class="article">
				Kurikulum program magister (S2) matematika memuat pengembangan matematika sebagai ilmu dasar dan penerapannya seperti : optimasi, pemodelan ekologi, matematika industri, sains komputasi dan matematika keuangan. 
Program Magister (S2) Matematika dapat diselesaikan dengan menempuh minimal 40 sks dalam 4 (empat) semester. Adapun mata kuliah yang ditawarkan adalah
				<br><br>
				<b>MATA KULIAH WAJIB (30 sks)</b>
				<ul>
					<li>Aljabar ( 3 sks)</li>
					<li>Analisis (3 sks)</li>
					<li>Kombinatorika Lanjut (3 sks)</li>
					<li>Pemodelan Matematika (3 sks)</li>
					<li>Analisis Numerik Lanjut (3 sks)</li>
					<li>Persamaan Diferensial (3 sks)</li>
					<li>Filsafat Ilmu dan metodelogi riset ( 3 sks)</li>
					<li>Probabiltas dan Stokastik (3 sks)</li>
					<li>Tesis (4 sks)</li>
				</ul>
				<b>MATA KULIAH PILIHAN</b>
				<ul>
					<li>Metode Kuantitatif untuk optimisasi</li>
					<li>Riset Operasi</li>
					<li>Komputasi dan Optimasi</li>
					<li>Sains Komputasi</li>
					<li>Sistem Kontrol Linear</li>
					<li>Kontrol Optimum</li>
					<li>Kontrol Tak Linear</li>
					<li>Logika Fuzzy dan Aplikasinya</li>
					<li>Matematika  Arbitrase</li>
					<li>Matematika Finansial</li>
					<li>Matematika  Aktuaria</li>
				</ul>

				<b>PEMINATAN S2 MATEMATIKA  TERAPAN</b>
				<ul>
					<li>Matematika Ekologi</li>
					<li>Sains Komputasi</li>
					<li>Matematika Keuangan</li>
					<li>Teori kontrol</li>
				</ul><br>
				Secara umum, peminatan Program S2 Matematika Terapan menawarkan pengembangan matematika murni (Aljabar, Kombinatorika & Analisis) sebagai tools untuk menyelesaikan masalah di bidang ekonomi, industri, teknologi, lingkungan, dan sosial.

